import { simpleFrequencySort } from "../src/simpleFrequencySort";

describe("自分の回答", () => {
  test("", () => {
    expect(simpleFrequencySort([2, 3, 5, 3, 7, 9, 5, 3, 7])).toEqual([
      3,
      3,
      3,
      5,
      5,
      7,
      7,
      2,
      9,
    ]);
  });

  test("出現する回数が同じ場合、数字の小さい順に並べる", () => {
    expect(
      simpleFrequencySort([
        37,
        37,
        37,
        37,
        1,
        1,
        1,
        11,
        11,
        11,
        20,
        20,
        20,
        29,
        29,
        29,
        49,
        49,
        49,
        2,
        2,
        12,
        12,
        21,
        13,
        13,
        21,
        26,
        36,
        36,
        41,
        41,
        4,
        5,
        9,
        10,
        14,
        16,
        17,
        18,
        19,
        22,
        25,
        27,
        28,
        45,
        32,
        39,
        42,
        44,
      ])
    ).toEqual([
      37,
      37,
      37,
      37,
      1,
      1,
      1,
      11,
      11,
      11,
      20,
      20,
      20,
      29,
      29,
      29,
      49,
      49,
      49,
      2,
      2,
      12,
      12,
      13,
      13,
      21,
      21,
      36,
      36,
      41,
      41,
      4,
      5,
      9,
      10,
      14,
      16,
      17,
      18,
      19,
      22,
      25,
      26,
      27,
      28,
      32,
      39,
      42,
      44,
      45,
    ]);
  })
});
